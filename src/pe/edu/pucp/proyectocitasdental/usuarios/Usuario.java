package pe.edu.pucp.proyectocitasdental.usuarios;
import java.util.*;

public abstract class Usuario {

    /**
     * @return the id_usuario
     */
    public int getId_usuario() {
        return id_usuario;
    }

    /**
     * @param id_usuario the id_usuario to set
     */
    public void setId_usuario(int id_usuario) {
        this.id_usuario = id_usuario;
    }

    /**
     * @return the nombres
     */
    public String getNombres() {
        return nombres;
    }

    /**
     * @param nombres the nombres to set
     */
    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    /**
     * @return the apellidos
     */
    public String getApellidos() {
        return apellidos;
    }

    /**
     * @param apellidos the apellidos to set
     */
    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    /**
     * @return the dni
     */
    public int getDni() {
        return dni;
    }

    /**
     * @param dni the dni to set
     */
    public void setDni(int dni) {
        this.dni = dni;
    }

    /**
     * @return the telefono
     */
    public int getTelefono() {
        return telefono;
    }

    /**
     * @param telefono the telefono to set
     */
    public void setTelefono(int telefono) {
        this.telefono = telefono;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the direccion
     */
    public String getDireccion() {
        return direccion;
    }

    /**
     * @param direccion the direccion to set
     */
    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    /**
     * @return the sexo
     */
    public char getSexo() {
        return sexo;
    }

    /**
     * @param sexo the sexo to set
     */
    public void setSexo(char sexo) {
        this.sexo = sexo;
    }

    private static int num_correlativo = 0;
    private int id_usuario;
    private String nombres;
    private String apellidos;
    private int dni;
    private int telefono;
    private String email;
    private String direccion;
    private Date fecha_nacimiento;
    private char sexo;

    
    public Usuario(String nombres, String apellidos, int dni, int telefono,
                   String email, String direccion, Date fecha_nacimiento,
                   char sexo){
        num_correlativo++;
        this.id_usuario=num_correlativo;
        this.nombres=nombres;
        this.apellidos=apellidos;
        this.dni=dni;
        this.telefono=telefono;
        this.email=email;
        this.direccion=direccion;
        this.fecha_nacimiento=fecha_nacimiento;
        this.sexo=sexo;
    }

    

    
    
}


